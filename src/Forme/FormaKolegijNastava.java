    public void napuniComboBox()
    {
    
    tipoviNastave = Main.db.dohvatiTipNastave();
    
   
    if(tipoviNastave.isEmpty())
    {
        JOptionPane.showMessageDialog(this,"Nije unesen niti jedan tip nastave!","Nema tipova nastave", JOptionPane.WARNING_MESSAGE);
        this.jComboBox1.setEnabled(false);
    }
    else
    {
        for(int i=0; i < tipoviNastave.size();i++)
        {
            String labela = tipoviNastave.get(i).getNaziv();
            if(tipoviNastave.get(i).isObavezno())
            {
                labela += " - obavez. ";
               
            }
            else
            {
                labela += " - neobavez. ";
                
            }
            
            jComboBox1.addItem(labela);
         
        }
                      
        
        jComboBox1.setSelectedIndex(0);
        odabraniTipNastave = jComboBox1.getSelectedItem().toString(); 
        

        slusaj = true;
    }
    
    }
    
    public void napuniTablicu()
    {
        ArrayList <Nastava> podaci = Main.db.dohvatiKolegijNastave(odabraniTipNastave,odabraniKolegijID);
        

        
        DefaultTableModel model = (DefaultTableModel) this.tablica.getModel();
        
        Object [] row = new Object [9];
        
        for(int i=0;i<podaci.size();i++)
        {
            row[0]=podaci.get(i).getIdNastava();
            row[1]=podaci.get(i).getOpis();
            Date datum=podaci.get(i).getDatumVrijeme().getDatum1();
            DateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");  
            row[2] = dateFormat.format(datum);
            row[3]=podaci.get(i).getDatumVrijeme().getVrijeme1();
            row[4]=podaci.get(i).getDatumVrijeme().getVrijeme2();
            row[5]=podaci.get(i).getProstorija();
            row[6]=podaci.get(i).getLokacija();
            row[7]=podaci.get(i).getDjelatnik();
            row[8]=podaci.get(i).getBrPrisutnih();
           
            
            model.addRow(row);
        }
        tablica.getColumnModel().getColumn(0).setMinWidth(0);
        tablica.getColumnModel().getColumn(0).setMaxWidth(0);
        tablica.getColumnModel().getColumn(0).setWidth(0);
        
          
        }
        
        public void ocistiTablicu()
        {
        DefaultTableModel model = (DefaultTableModel) tablica.getModel();
        model.setRowCount(0); 
        }
       
        
  

 


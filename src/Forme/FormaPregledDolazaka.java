    public void napuniTablicu()
    {
    
        DateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");  
         
        
        
        int k = Main.formaPrijava.glavnaforma.dolasci.getTablica().getSelectedRow();
        TableModel model = Main.formaPrijava.glavnaforma.dolasci.getTablica().getModel();
      
        String jmbag =  model.getValueAt(k,0).toString();

        String idKolegij = Main.formaPrijava.glavnaforma.odabraniKolegijID;
         
        
        System.out.println(jmbag+" "+idKolegij);
        
        
        ArrayList <Dolazak> dolasci = Main.db.dohvatiStudentDolaske(jmbag,idKolegij);
       
        DefaultTableModel model_ = (DefaultTableModel) this.tablica.getModel();
        
        Object [] row = new Object [5];
        
        for(int i=0;i<dolasci.size();i++)
        {
            row[0]=dolasci.get(i).getTipNastava();
            row[1]=dolasci.get(i).getOpis();
            Date datum =dolasci.get(i).getDatumOdrz();
            String strDatum = dateFormat.format(datum); 
            row[2]=strDatum;
            row[3]=dolasci.get(i).getObavezno();
            row[4]=dolasci.get(i).getPrisutan();
      
           
            model_.addRow(row);
        }
      
    }
    
     
